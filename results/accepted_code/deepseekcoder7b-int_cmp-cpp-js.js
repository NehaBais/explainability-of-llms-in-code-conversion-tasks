let a = parseInt(prompt("Enter the first integer"));
let b = parseInt(prompt("Enter the second integer"));

// test for less-than
if (a < b) {
  console.log(`${a} is less than ${b}.`);
}

// test for equality
if (a === b) {
  console.log(`${a} is equal to ${b}.`);
}

// test for greater-than
if (a > b) {
  console.log(`${a} is greater than ${b}.`);
}
