let a = parseInt(prompt("Enter first integer"));
let b = parseInt(prompt("Enter second integer"));

console.log(`a+b = ${a + b}`);
console.log(`a-b = ${a - b}`);
console.log(`a*b = ${a * b}`);
// In JavaScript, the quotient rounds towards zero
console.log(`a/b = ${Math.floor(a / b)} and remainder is ${a % b}`);
