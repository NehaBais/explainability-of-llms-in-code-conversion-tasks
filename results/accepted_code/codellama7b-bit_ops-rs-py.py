if __name__ == "__main__":
    a = 105
    b = 91
    print("a      =", bin(a))
    print("b      =", bin(b))
    print("a | b  =", bin(a | b))
    print("a & b  =", bin(a & b))
    print("a ^ b  =", bin(a ^ b))
    print("!a     =", bin(~a))
    print("a << 3 =", bin(a << 3))
    print("a >> 3 =", bin(a >> 3))
