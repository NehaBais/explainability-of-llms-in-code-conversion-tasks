let nbr1 = parseInt(prompt("Enter the first number"));
let nbr2 = parseInt(prompt("Enter the second number"));

if (nbr1 < nbr2) {
  console.log(`${nbr1} is less than ${nbr2}`);
}

if (nbr1 > nbr2) {
  console.log(`${nbr1} is greater than ${nbr2}`);
}

if (nbr1 === nbr2) {
  console.log(`${nbr1} is equal to ${nbr2}`);
}
