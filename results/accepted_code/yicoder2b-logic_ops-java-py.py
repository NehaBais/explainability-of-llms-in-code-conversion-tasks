def logic(a, b):
    print(
        f"a AND b: {int(a and b)}"
    )  # In python "and" returns True only if both sides are true.
    print(
        f"a OR b: {int(a or b)}"
    )  # In Python "or" returns True if either of the sides are true.
    print(
        f"NOT a: {int(not a)}"
    )  # "!" in python is equivalent to "not" keyword in java.
