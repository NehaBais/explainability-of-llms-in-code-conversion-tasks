a = int(input("Enter first integer: "))
b = int(input("Enter second integer: "))

print(f"sum:              {a + b}")
print(f"difference:       {a - b}")
print(f"product:          {a * b}")
print(f"integer quotient: {a // b}")  # truncates towards zero
print(f"remainder:        {a % b}")  # same sign as first operand
