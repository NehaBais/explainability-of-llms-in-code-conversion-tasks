// Getting integer inputs from user
var a = prompt("Enter value of a:");
var b = prompt("Enter value of b:");

// Comparing the numbers
if (a < b) {
  console.log("a is less than b");
} else if (a > b) {
  console.log("a is greater than b");
} else if (a === b) {
  console.log("a is equal to b");
}
