a = int(input("Enter first number: "))  # Take input for a
b = int(input("Enter second number: "))  # Take input for b

print("a+b =", a + b)
print("a-b =", a - b)
print("a*b =", a * b)
print(
    "a/b (floor division)=", a // b, ", remainder=", a % b
)  # Floor division and modulus operation
print(
    "a**b (exponentiation) =", a**b
)  # Exponentiation using ** operator. Python has no ^ operator to perform exponentiation.
