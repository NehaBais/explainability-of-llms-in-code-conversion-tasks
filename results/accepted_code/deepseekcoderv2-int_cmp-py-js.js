let a = prompt("Enter value of a: ");
let b = prompt("Enter value of b: ");

if (parseInt(a) < parseInt(b)) {
  console.log("a is less than b");
} else if (parseInt(a) > parseInt(b)) {
  console.log("a is greater than b");
} else if (parseInt(a) == parseInt(b)) {
  console.log("a is equal to b");
}
