public class Main {
  public static void main(String[] args) {
    int a = 105;
    int b = 91;

    System.out.printf("a      = %8d%n", a);
    System.out.printf("b      = %8d%n", b);
    System.out.printf("a | b  = %8d%n", a | b);
    System.out.printf("a & b  = %8d%n", a & b);
    System.out.printf("a ^ b  = %8d%n", a ^ b);
    System.out.printf("!a     = %8d%n", !a);
    System.out.printf("a << 3  = %8d%n", a << 3);
    System.out.printf("a >> 3  = %8d%n", a >> 3);
  }
}
