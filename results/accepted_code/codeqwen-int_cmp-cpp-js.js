let A, b;

A = parseInt(prompt("Enter first Integer"));
b = parseInt(prompt("Enter Second Integer"));

// test for less-than
if (A < b) {
  console.log(A + " Is less than " + b);
}

// test for equality
if (A === b) {
  console.log(A + " Is equal to " + b);
}

// test for greater-than
if (A > b) {
  console.log(A + " Is greater than " + b);
}
