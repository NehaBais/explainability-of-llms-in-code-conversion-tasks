# get two integers from user
a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

print(f"a+b = {a + b}")
print(f"a-b = {a - b}")
print(f"a*b = {a * b}")
print(f"a//b = {a // b}")  # integer quotient, rounds towards zero
print(f"a%b = {a % b}")  # remainder, same sign as first operand
