const ball = "ball";
const bell = "bell";

console.log(`${ball} ${STREQ(ball, bell)}`);
console.log(`${bell} ${STRNE(ball, bell)}`);
console.log(`${ball} ${STRLT(ball, bell)}`);
console.log(`${bell} ${STRGT(ball, bell)}`);
console.log(`${ball} ${STRGE(ball, bell)}`);
console.log(`${bell} ${STRLE(ball, bell)}`);
